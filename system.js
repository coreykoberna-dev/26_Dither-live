function createNamedPixelIcon(name, options = {}) {
  const icon = getPixelIcon(name);
  if (!icon) return null;

  const svg = createPixelIcon(icon);
  if (options.hidden !== false) {
    svg.setAttribute("aria-hidden", "true");
    svg.removeAttribute("aria-label");
    svg.setAttribute("focusable", "false");
  }
  if (options.className) {
    for (const className of String(options.className).split(/\s+/).filter(Boolean)) {
      svg.classList.add(className);
    }
  }
  return svg;
}

function hydratePixelIcons(root = document) {
  const targets = root.matches?.("[data-pixel-icon]")
    ? [root]
    : [...root.querySelectorAll("[data-pixel-icon]")];

  for (const target of targets) {
    const iconName = target.dataset.pixelIcon;
    const svg = createNamedPixelIcon(iconName, {
      hidden: target.getAttribute("aria-hidden") !== "false",
    });
    if (!svg) {
      target.dataset.pixelIconMissing = "true";
      continue;
    }
    delete target.dataset.pixelIconMissing;
    target.replaceChildren(svg);
  }
}

