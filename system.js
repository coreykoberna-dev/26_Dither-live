function clampDynamic(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function normalizeDynamicHue(hue) {
  return ((hue % 360) + 360) % 360;
}

function dynamicOklch(lightness, chroma, hue, alpha = 1) {
  const l = clampDynamic(lightness, 0, 100).toFixed(1);
  const c = Math.max(0, chroma).toFixed(3);
  const h = normalizeDynamicHue(hue).toFixed(1);
  if (alpha < 1) return `oklch(${l}% ${c} ${h} / ${alpha.toFixed(3)})`;
  return `oklch(${l}% ${c} ${h})`;
}

function dynamicAlpha(color, alpha) {
  return color.replace(/\)$/, ` / ${alpha})`);
}

function toneChroma(baseChroma, tone) {
  if (tone <= 0 || tone >= 100) return 0;
  const softEdge = Math.sin((tone / 100) * Math.PI);
  return baseChroma * Math.pow(Math.max(0, softEdge), 0.58);
}

function createTonalPalettes(seed) {
  const baseChroma = clampDynamic(0.08 + seed.saturation * 0.17, 0.1, 0.23);
  const neutralChroma = clampDynamic(0.012 + seed.saturation * 0.03, 0.012, 0.048);
  const variantChroma = clampDynamic(0.034 + seed.saturation * 0.058, 0.034, 0.09);
  const palettes = {};

  TONAL_PALETTE_DEFS.forEach(([name, hueShift, chromaRatio]) => {
    const hue = name === "error" ? 28 : normalizeDynamicHue(seed.hue + hueShift);
    const familyChroma = name === "neutral"
      ? neutralChroma
      : name === "neutral-variant"
        ? variantChroma
        : name === "error"
          ? 0.18
          : baseChroma * chromaRatio;

    palettes[name] = Object.fromEntries(
      ALL_DYNAMIC_COLOR_TONES.map((tone) => [tone, dynamicOklch(tone, toneChroma(familyChroma, tone), hue)]),
    );
  });

  return palettes;
}

function pickTone(palettes, family, tone) {
  return palettes[family]?.[tone] || dynamicOklch(tone, 0, 0);
}

