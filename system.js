function applyMaterialGeometryBlueprints() {
  for (const icon of PIXEL_ICONS) {
    icon.benchmark = materialBenchmarkForIcon(icon);
    const precisionRects = precisionPixelGlyphForIcon(icon);
    if (precisionRects) {
      icon.rects = precisionRects;
      delete icon.cells;
      icon.source = "material-benchmark-precision-pixel";
      continue;
    }
    const shapes = materialShapesForIcon(icon);
    if (!shapes) continue;
    icon.rects = rasterizeMaterialGeometry(shapes.flat());
    delete icon.cells;
    icon.source = "material-geometry-pixel-texture";
  }
}

function slugifyIconName(value) {
  return String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

