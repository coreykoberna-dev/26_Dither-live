function iconLibraryCategories() {
  return ["All", ...new Set(DITHER_ICON_LIBRARY.map((icon) => icon.category))];
}

function createExplorerButton(label, active, onClick) {
  const button = document.createElement("button");
  button.type = "button";
  button.textContent = label;
  button.setAttribute("aria-pressed", active ? "true" : "false");
  if (active) button.classList.add("is-active");
  button.addEventListener("click", onClick);
  return button;
}

function filteredIconLibrary() {
  const query = iconExplorerState.query.trim().toLowerCase();
  return DITHER_ICON_LIBRARY.filter((icon) => {
    if (icon.style !== iconExplorerState.style) return false;
    if (iconExplorerState.category !== "All" && icon.category !== iconExplorerState.category) return false;
    if (!query) return true;
    return `${icon.name} ${icon.category} ${icon.tags}`.toLowerCase().includes(query);
  });
}

function createLibraryIconCard(icon) {
  const item = document.createElement("article");
  item.className = "icon-library-card";
  item.dataset.style = icon.style;
  item.dataset.category = icon.category;

  const tile = document.createElement("span");
  tile.className = "icon-pixel-tile";
  tile.appendChild(createPixelIcon(icon));

  const name = document.createElement("strong");
  name.textContent = icon.name;
  const meta = document.createElement("span");
  meta.textContent = icon.source?.startsWith("pixelarticons")
    ? icon.category
    : `${icon.category} / ${icon.styleName}`;

  item.append(tile, name, meta);
  return item;
}

function renderIconLibraryTabs(categories = iconLibraryCategories()) {
  const styleTabs = document.getElementById("iconStyleTabs");
  const categoryTabs = document.getElementById("iconCategoryTabs");
  if (styleTabs) {
    styleTabs.innerHTML = "";
    for (const style of activeIconLibraryStyles()) {
      styleTabs.appendChild(createExplorerButton(style.name, style.id === iconExplorerState.style, () => {
        iconExplorerState = { ...iconExplorerState, style: style.id, visible: ICON_LIBRARY_PAGE_SIZE };
        renderIconLibraryExplorer();
      }));
    }
  }
  if (categoryTabs) {
    categoryTabs.innerHTML = "";
    for (const category of categories) {
      categoryTabs.appendChild(createExplorerButton(category, category === iconExplorerState.category, () => {
        iconExplorerState = { ...iconExplorerState, category, visible: ICON_LIBRARY_PAGE_SIZE };
        renderIconLibraryExplorer();
      }));
    }
  }
}

function renderIconLibraryExplorer() {
  const grid = document.getElementById("iconLibraryGrid");
  const result = document.getElementById("iconResultCount");
  const loadMore = document.getElementById("iconLoadMoreButton");
  const libraryCount = document.getElementById("iconLibraryCount");
  const foundationCount = document.getElementById("iconFoundationCount");
  const categoryCount = document.getElementById("iconCategoryCount");
  const iconCount = document.getElementById("iconCount");
  const search = document.getElementById("iconSearchInput");
  if (!grid) return;
  const foundationIcons = activeFoundationIcons();
  const categories = iconLibraryCategories();

  if (search && search.value !== iconExplorerState.query) search.value = iconExplorerState.query;
  if (libraryCount) libraryCount.textContent = `${DITHER_ICON_LIBRARY.length} library icons`;
  if (foundationCount) foundationCount.textContent = `${foundationIcons.length} installed glyphs`;
  if (categoryCount) categoryCount.textContent = `${categories.length - 1} categories`;
  if (iconCount) iconCount.textContent = `${foundationIcons.length} glyphs`;

  renderIconLibraryTabs(categories);

  const filtered = filteredIconLibrary();
  const visible = filtered.slice(0, iconExplorerState.visible);
  grid.innerHTML = "";
  for (const icon of visible) grid.appendChild(createLibraryIconCard(icon));

  if (result) result.textContent = `${visible.length} of ${filtered.length} shown`;
  if (loadMore) {
    loadMore.hidden = visible.length >= filtered.length;
    loadMore.textContent = `Show ${Math.min(ICON_LIBRARY_PAGE_SIZE, filtered.length - visible.length)} more`;
  }
}

function initIconLibraryExplorer() {
  const search = document.getElementById("iconSearchInput");
  const loadMore = document.getElementById("iconLoadMoreButton");
  if (search) {
    search.addEventListener("input", () => {
      iconExplorerState = { ...iconExplorerState, query: search.value, visible: ICON_LIBRARY_PAGE_SIZE };
      renderIconLibraryExplorer();
    });
  }
  if (loadMore) {
    loadMore.addEventListener("click", () => {
      iconExplorerState = { ...iconExplorerState, visible: iconExplorerState.visible + ICON_LIBRARY_PAGE_SIZE };
      renderIconLibraryExplorer();
    });
  }
  renderIconLibraryExplorer();
}

