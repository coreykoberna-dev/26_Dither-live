function distanceToSegment(px, py, x1, y1, x2, y2) {
  const dx = x2 - x1;
  const dy = y2 - y1;
  const lengthSq = dx * dx + dy * dy;
  if (!lengthSq) return Math.hypot(px - x1, py - y1);
  const t = Math.max(0, Math.min(1, ((px - x1) * dx + (py - y1) * dy) / lengthSq));
  return Math.hypot(px - (x1 + t * dx), py - (y1 + t * dy));
}

function pointInPolygon(x, y, points) {
  let inside = false;
  for (let i = 0, j = points.length - 1; i < points.length; j = i, i += 1) {
    const [xi, yi] = points[i];
    const [xj, yj] = points[j];
    const intersects = yi > y !== yj > y && x < ((xj - xi) * (y - yi)) / (yj - yi || 1) + xi;
    if (intersects) inside = !inside;
  }
  return inside;
}

function materialShapeContains(shape, x, y) {
  if (shape.type === "line") return distanceToSegment(x, y, shape.x1, shape.y1, shape.x2, shape.y2) <= shape.width / 2;
  if (shape.type === "polyline") {
    for (let i = 1; i < shape.points.length; i += 1) {
      const [x1, y1] = shape.points[i - 1];
      const [x2, y2] = shape.points[i];
      if (distanceToSegment(x, y, x1, y1, x2, y2) <= shape.width / 2) return true;
    }
    return false;
  }
  if (shape.type === "rect") {
    const inside = x >= shape.x && x <= shape.x + shape.width && y >= shape.y && y <= shape.y + shape.height;
    if (!inside) return false;
    if (shape.mode === "fill") return true;
    return (
      x <= shape.x + shape.stroke ||
      x >= shape.x + shape.width - shape.stroke ||
      y <= shape.y + shape.stroke ||
      y >= shape.y + shape.height - shape.stroke
    );
  }
  if (shape.type === "circle") {
    const distance = Math.hypot(x - shape.cx, y - shape.cy);
    if (shape.mode === "fill") return distance <= shape.radius;
    return Math.abs(distance - shape.radius) <= shape.stroke / 2;
  }
  if (shape.type === "polygon") return pointInPolygon(x, y, shape.points);
  return false;
}

function rasterizeMaterialGeometry(shapes) {
  const grid = emptyIconGrid();
  const additive = shapes.filter((shape) => shape.op !== "subtract");
  const subtractive = shapes.filter((shape) => shape.op === "subtract");
  const cellSize = MATERIAL_PIXEL_GRID.sourceSize / MATERIAL_PIXEL_GRID.productionCells;
  for (let y = 0; y < MATERIAL_PIXEL_GRID.productionCells; y += 1) {
    for (let x = 0; x < MATERIAL_PIXEL_GRID.productionCells; x += 1) {
      const cx = (x + 0.5) * cellSize;
      const cy = (y + 0.5) * cellSize;
      if (
        additive.some((shape) => materialShapeContains(shape, cx, cy)) &&
        !subtractive.some((shape) => materialShapeContains(shape, cx, cy))
      ) {
        grid[y][x] = true;
      }
    }
  }
  return pixelGridToRects(grid);
}

