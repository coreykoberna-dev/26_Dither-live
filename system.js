function materialPixelGlyphs(glyphs) {
  return glyphs.map(({ cells, ...icon }) => ({
    ...icon,
    source: `${cellGridSize(cells)}-pixel-grid`,
    cells,
    rects: rectsFromCells(cells),
  }));
}

function pixelRows(source) {
  return String(source)
    .trim()
    .split("\n")
    .map((row) => row.trim())
    .filter(Boolean);
}

function cellGridSize(rows) {
  const rowLength = rows.reduce((max, row) => Math.max(max, String(row).length), 0);
  const gridSize = Math.max(rows.length, rowLength, MATERIAL_PIXEL_GRID.prototypeCells);
  return gridSize <= MATERIAL_PIXEL_GRID.prototypeCells ? MATERIAL_PIXEL_GRID.prototypeCells : MATERIAL_PIXEL_GRID.productionCells;
}

function normalizedCellRow(rows, y, gridSize) {
  return String(rows[y] || "").padEnd(gridSize, ".").slice(0, gridSize);
}

function rectsFromCells(rows) {
  const rects = [];
  const gridSize = cellGridSize(rows);
  const moduleSize = MATERIAL_PIXEL_GRID.sourceSize / gridSize;
  for (let y = 0; y < gridSize; y += 1) {
    const row = normalizedCellRow(rows, y, gridSize);
    let start = -1;
    for (let x = 0; x <= gridSize; x += 1) {
      const filled = row[x] === "#";
      if (filled && start === -1) start = x;
      if ((!filled || x === gridSize) && start !== -1) {
        rects.push([
          start * moduleSize,
          y * moduleSize,
          (x - start) * moduleSize,
          moduleSize,
        ]);
        start = -1;
      }
    }
  }
  return rects;
}

function countFilledCells(cells = []) {
  return cells.reduce((total, row) => total + [...String(row)].filter((cell) => cell === "#").length, 0);
}

function countRectPixels(rects = []) {
  const cellSize = MATERIAL_PIXEL_GRID.sourceSize / MATERIAL_PIXEL_GRID.productionCells;
  return rects.reduce((total, [, , width, height]) => total + Math.round((width * height) / (cellSize * cellSize)), 0);
}

function precisionApply(grid, matcher, op = "add") {
  const cells = MATERIAL_PIXEL_GRID.productionCells;
  const cellSize = MATERIAL_PIXEL_GRID.sourceSize / cells;
  for (let y = 0; y < cells; y += 1) {
    for (let x = 0; x < cells; x += 1) {
      const cx = (x + 0.5) * cellSize;
      const cy = (y + 0.5) * cellSize;
      if (matcher(cx, cy)) grid[y][x] = op === "subtract" ? false : true;
    }
  }
}

function precisionLine(grid, x1, y1, x2, y2, width = MATERIAL_PIXEL_GRID.stroke, op = "add") {
  precisionApply(grid, (x, y) => distanceToSegment(x, y, x1, y1, x2, y2) <= width / 2, op);
}

function precisionPolyline(grid, points, width = MATERIAL_PIXEL_GRID.stroke, op = "add") {
  for (let index = 1; index < points.length; index += 1) {
    const [x1, y1] = points[index - 1];
    const [x2, y2] = points[index];
    precisionLine(grid, x1, y1, x2, y2, width, op);
  }
}

function precisionRect(grid, x, y, width, height, mode = "stroke", stroke = MATERIAL_PIXEL_GRID.stroke, op = "add") {
  precisionApply(grid, (px, py) => {
    const inside = px >= x && px < x + width && py >= y && py < y + height;
    if (!inside) return false;
    if (mode === "fill") return true;
    return px < x + stroke || px >= x + width - stroke || py < y + stroke || py >= y + height - stroke;
  }, op);
}

function precisionCircle(grid, cx, cy, radius, mode = "stroke", stroke = MATERIAL_PIXEL_GRID.stroke, op = "add") {
  precisionApply(grid, (x, y) => {
    const distance = Math.hypot(x - cx, y - cy);
    if (mode === "fill") return distance <= radius;
    return Math.abs(distance - radius) <= stroke / 2;
  }, op);
}

function precisionPolygon(grid, points, op = "add") {
  precisionApply(grid, (x, y) => pointInPolygon(x, y, points), op);
}

function precisionDots(grid, points, size = 2, op = "add") {
  for (const [x, y] of points) precisionRect(grid, x, y, size, size, "fill", MATERIAL_PIXEL_GRID.stroke, op);
}

function precisionIconRects(draw) {
  const grid = emptyIconGrid();
  const api = {
    line: (...args) => precisionLine(grid, ...args),
    polyline: (...args) => precisionPolyline(grid, ...args),
    rect: (...args) => precisionRect(grid, ...args),
    circle: (...args) => precisionCircle(grid, ...args),
    polygon: (...args) => precisionPolygon(grid, ...args),
    dots: (...args) => precisionDots(grid, ...args),
  };
  draw(api);
  return pixelGridToRects(grid);
}

function materialSymbolNameForIcon(icon) {
  return MATERIAL_BENCHMARK_ALIASES[icon.name] || icon.name.toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_+|_+$/g, "");
}

function materialBenchmarkForm(icon, symbol) {
  const name = icon.name.toLowerCase();
  if (name.includes("arrow") || name.includes("chevron") || name.includes("directions") || name.includes("navigation")) return "directional arrow";
  if (["play", "fast_forward", "fast_rewind", "skip_next", "skip_previous"].some((token) => symbol.includes(token))) return "media transport";
  if (["image", "videocam", "desktop_windows", "smartphone", "tablet_mac", "draft", "folder", "archive", "mail"].includes(symbol)) return "rectangular container";
  if (["info", "error", "check_circle", "radio_button_checked", "schedule", "public", "explore"].includes(symbol)) return "20dp circle keyline";
  if (["warning", "navigation", "send", "play_arrow"].includes(symbol)) return "filled triangle";
  if (icon.category === "Dither") return "adapted product metaphor";
  if (icon.category === "Text Formatting") return "typographic control";
  if (icon.category === "Weather") return "weather glyph";
  return "Material system symbol";
}

function materialBenchmarkStatus(icon, symbol) {
  if (MATERIAL_BENCHMARK_ADAPTED.has(icon.name)) return "adapted";
  if (MATERIAL_BENCHMARK_ALIASES[icon.name] || symbol === icon.name.toLowerCase()) return "direct";
  return "inferred";
}

function materialBenchmarkForIcon(icon) {
  const symbol = materialSymbolNameForIcon(icon);
  const status = materialBenchmarkStatus(icon, symbol);
  return {
    symbol,
    status,
    form: materialBenchmarkForm(icon, symbol),
    criteria: "match Material metaphor, keyline balance, counter placement, and stroke weight before adding pixel texture",
  };
}

