function applyIconRevisions() {
  for (const icon of PIXEL_ICONS) {
    const revision = SALIENT_ICON_REVISIONS.get(icon.name);
    if (!revision) continue;
    icon.cells = revision.cells;
    icon.rects = revision.cells ? rectsFromCells(revision.cells) : revision.rects;
    icon.source = revision.cells ? `${cellGridSize(revision.cells)}-pixel-salience-grid` : "hand-hinted-24-grid";
  }
}

